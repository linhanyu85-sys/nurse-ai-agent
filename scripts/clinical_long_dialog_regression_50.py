def _agent_case(
    *,
    name: str,
    scene: str,
    ward_summary: str,
    patient_points: list[str],
    pain_points: list[str],
    asks: list[str],
    constraints: list[str],
    keywords: tuple[str, ...],
    artifact_kinds: tuple[str, ...] = ("document_plan",),
) -> RegressionCase:
    return RegressionCase(
        name=name,
        category="长对话·AI Agent",
        mode="agent_cluster",
        execution_profile="full_loop",
        user_input=build_long_prompt(
            scene=scene,
            ward_summary=ward_summary,
            patient_points=patient_points,
            pain_points=pain_points,
            asks=asks,
            constraints=constraints,
        ),
        expect_workflows=("autonomous_care",),
        expected_keywords=keywords,
        expect_artifact_kinds=artifact_kinds,
        min_answer_length=240,
        min_prompt_length=560,
        require_context_hit=True,
        max_elapsed_sec=90,
    )


